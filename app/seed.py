def seed_if_empty(session: Session) -> None:
    # ProductBlock — force-reseed на каждом старте, чтобы правки в коде гарантированно доехали до прода.
    # FK на product_blocks нет, удаление безопасно.
    session.query(ProductBlock).delete()
    session.add_all([ProductBlock(**p) for p in PRODUCTS])
    if session.query(MessageTemplate).count() == 0:
        session.add_all([MessageTemplate(**t) for t in TEMPLATES])
    if session.query(FaqItem).count() == 0:
        session.add_all([FaqItem(**f) for f in FAQ])
    session.commit()
